    public void mostrarDatos(String consulta){
        m = (DefaultTableModel) tblPais.getModel();
        libreria.conectarBaseDatos();
        ResultSet resultado = libreria.getTablaPais(consulta);
        m.setColumnIdentifiers(new Object[]{"Clave","Nombre País"});
        try{
            while(resultado.next())
                m.addRow(new Object[]{resultado.getInt("idpais"),resultado.getString("nombrepais")});
            
            tblPais.setFont(new Font("Verdana",Font.BOLD,22));
            tblPais.setRowHeight(26);
            tblPais.setModel(m);
        }catch(SQLException sqle){
            showMessageDialog(this, "Error: "+sqle);
        }
    }
            
