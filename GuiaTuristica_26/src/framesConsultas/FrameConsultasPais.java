    private DefaultTableModel m = new DefaultTableModel();

    private void limpiarTabla(){
        int rowCount = m.getRowCount();
        //Remove rows one by one from the end of the table
        for (int i = rowCount - 1; i >= 0; i--) {
            m.removeRow(i);
}
    }
    
