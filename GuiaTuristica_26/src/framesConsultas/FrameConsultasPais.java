    private void txtPaisKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtPaisKeyPressed
        if(evt.getKeyCode() == KeyEvent.VK_ENTER){
            if(!txtPais.getText().equals("")){
                String nombre = txtPais.getText(); 
                limpiarTabla();
                mostrarDatos("Select idpais, nombrepais "
                + "from pais where nombrepais like '"+nombre+"';");
            }
        }
        if(txtPais.getText().isEmpty()){
            limpiarTabla();
            mostrarDatos("Select idpais, nombrepais from pais");
        }
    }//GEN-LAST:event_txtPaisKeyPressed

    private void txtPaisKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtPaisKeyReleased
         if(txtPais.getText().isEmpty()){
            limpiarTabla();
            mostrarDatos("Select idpais, nombrepais from pais");
        }
    }//GEN-LAST:event_txtPaisKeyReleased

